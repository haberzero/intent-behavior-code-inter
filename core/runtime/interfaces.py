from typing import Any, Protocol, List, Dict, Callable, Optional, Type, Union, runtime_checkable, Mapping
from core.domain import ast as ast
from core.foundation.interfaces import (
    IssueTracker, IStackInspector, ISourceProvider, ICompilerService
)

@runtime_checkable
class Registry(Protocol):
    """类与实例注册表接口"""
    def get_class(self, name: str) -> Any: ...
    def box(self, value: Any, memo: Optional[Dict[int, Any]] = None) -> Any: ...

# --- Runtime Value Symbols ---

class RuntimeSymbol(Protocol):
    """运行时符号，包含值和类型信息"""
    name: str
    value: Any
    declared_type: Any  # IBC-Inter 类型 (int, float, str, list, dict, var, etc.)
    current_type: Any   # 运行时实际类型
    is_const: bool      # 是否为常量（内置函数等）

class SymbolView(Protocol):
    """只读符号视图接口"""
    def get(self, name: str) -> Any: ...
    def get_symbol(self, name: str) -> Optional[RuntimeSymbol]: ...
    def has(self, name: str) -> bool: ...

# --- Scope & Context ---

class Scope(Protocol):
    """运行时作用域接口"""
    def define(self, name: str, value: Any, declared_type: Any = None, is_const: bool = False, uid: Optional[str] = None, force: bool = False) -> None: ...
    def assign(self, name: str, value: Any) -> bool: ...
    def get(self, name: str) -> Any: ...
    def get_symbol(self, name: str) -> Optional[RuntimeSymbol]: ...
    def get_all_symbols(self) -> Dict[str, RuntimeSymbol]: ...
    @property
    def parent(self) -> Optional['Scope']: ...

class RuntimeContext(Protocol):
    """解释器运行时上下文，管理作用域和意图栈"""
    def enter_scope(self) -> None: ...
    def exit_scope(self) -> None: ...
    def define_variable(self, name: str, value: Any, declared_type: Any = None, is_const: bool = False, uid: Optional[str] = None, force: bool = False) -> None: ...
    def set_variable(self, name: str, value: Any) -> None: ...
    def get_variable(self, name: str) -> Any: ...
    def get_symbol(self, name: str) -> Optional[RuntimeSymbol]: ...
    def get_vars(self) -> Dict[str, Any]: ...
    
    # 意图管理 (三层架构: Global, Block, Call)
    def set_global_intent(self, intent: 'IbIntent') -> None: ...
    def clear_global_intents(self) -> None: ...
    def remove_global_intent(self, intent: Union[str, 'IbIntent']) -> None: ...
    def get_global_intents(self) -> List['IbIntent']: ...
    
    # 意图屏蔽控制 (Block 级别)
    def enter_intent_exclusive_scope(self) -> None: ...
    def exit_intent_exclusive_scope(self) -> None: ...
    def is_intent_exclusive(self) -> bool: ...
    
    def push_intent(self, intent: Union[str, 'IbIntent'], mode: str = "+", tag: Optional[str] = None) -> None: ...
    def pop_intent(self) -> Optional['IbIntent']: ...
    def get_active_intents(self) -> List['IbIntent']: ...
    
    @property
    def intent_stack(self) -> List['ast.IbIntentInfo']: ...
    
    # 循环上下文注入
    def push_loop_context(self, index: int, total: int) -> None: ...
    def pop_loop_context(self) -> None: ...
    def get_loop_context(self) -> Optional[Dict[str, int]]: ...
    
    @property
    def current_scope(self) -> Scope: ...
    @property
    def global_scope(self) -> Scope: ...

    @property
    def registry(self) -> Any: ...

    def get_symbol_view(self) -> SymbolView: ...

# --- Components ---

class LLMExecutor(Protocol):
    """LLM 执行组件，负责构建 Prompt 并调用 LLM"""
    def execute_llm_function(self, node_uid: str, context: 'RuntimeContext') -> Any: ...
    def execute_behavior_expression(self, node_uid: str, context: 'RuntimeContext', captured_intents: Optional[List[str]] = None) -> Any: ...
    def get_last_call_info(self) -> Mapping[str, Any]: ...

class InterOp(Protocol):
    """Python 互操作组件，负责 Python 函数/类的映射和命名空间隔离"""
    def register_package(self, name: str, obj: Any) -> None: ...
    def get_package(self, name: str) -> Optional[Any]: ...
    def wrap_python_function(self, func: Callable) -> Callable: ...

class ModuleManager(Protocol):
    """模块管理组件，负责 import 逻辑"""
    def import_module(self, module_name: str, context: 'RuntimeContext') -> Any: ...
    def import_from(self, module_name: str, names: List[tuple], context: 'RuntimeContext') -> None: ...

class ModuleInstance(Protocol):
    """IBC-Inter 模块实例，表示一个已执行模块的运行时状态"""
    name: str
    scope: Scope
    def get_variable(self, name: str) -> Any: ...

class PermissionManager(Protocol):
    """权限管理组件，负责沙箱路径校验"""
    def validate_path(self, path: str, operation: str = "access") -> None: ...
    def enable_external_access(self) -> None: ...
    def is_external_access_enabled(self) -> bool: ...

class IHostService(Protocol):
    """内核级宿主服务子系统接口"""
    def sync(self) -> None: ...
    def save_state(self, path: str) -> None: ...
    def load_state(self, path: str) -> None: ...
    def run_isolated(self, path: str, policy: Dict[str, Any]) -> bool: ...
    def get_source(self) -> str: ...

class IInterpreterFactory(Protocol):
    """
    解释器实例工厂接口。
    用于解决 HostService 与 Interpreter 之间的物理循环依赖。
    """
    def spawn_interpreter(self, 
                          artifact: Any, 
                          registry: Any, 
                          host_interface: Any,
                          root_dir: str,
                          parent_context: 'RuntimeContext') -> 'Interpreter': ...

class IObjectFactory(Protocol):
    """
    运行时对象工厂接口。
    用于解耦 Service（如 ModuleManager）与具体运行时对象（如 IbModule）的构造。
    """
    def create_module(self, name: str, scope: Scope) -> Any: ...
    def create_scope(self, parent: Optional[Scope] = None) -> Scope: ...
    def create_native_object(self, py_obj: Any, ib_class: Any, vtable: Optional[Dict[str, Any]] = None) -> Any: ...

# --- Service Context ---

class ServiceContext(Protocol):
    """注入容器，聚合所有运行时服务"""
    def set_interpreter(self, interpreter: 'Interpreter') -> None: ...
    @property
    def interpreter(self) -> 'Interpreter': ...
    @property
    def registry(self) -> Any: ...
    @property
    def issue_tracker(self) -> IssueTracker: ...
    @property
    def runtime_context(self) -> RuntimeContext: ...
    @property
    def symbol_view(self) -> SymbolView: ...
    @property
    def llm_executor(self) -> LLMExecutor: ...
    @property
    def module_manager(self) -> ModuleManager: ...
    @property
    def object_factory(self) -> IObjectFactory: ...
    @property
    def interop(self) -> InterOp: ...
    @property
    def permission_manager(self) -> PermissionManager: ...
    @property
    def host_service(self) -> IHostService: ...
    @property
    def source_provider(self) -> ISourceProvider: ...
    @property
    def compiler(self) -> ICompilerService: ...

# --- Main Interpreter ---

class Interpreter(Protocol):
    """IBC-Inter 解释器协议"""
    @property
    def context(self) -> RuntimeContext: ...
    @property
    def registry(self) -> Any: ...
    def visit(self, node_uid: str) -> Any: ...
    def get_node_data(self, node_uid: str) -> Mapping[str, Any]: ...
    def get_side_table(self, table_name: str, key: str) -> Any: ...
    def execute_module(self, root_node_uid: str, module_name: str, scope: Any): ...
